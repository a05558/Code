#!/usr/bin/env python3


"""
Bars Module
===========
"""
def starbar(num):
    """Print * SplitLine
    :arg num: lenght
    """
    print('*' * num)

def hashbar(num):
    """Print # SplitLine
    :arg num:Line Lenght
    """
    print('#' * num)

def simplebar(num):
    """Print - Split Line

    :arg num:Line Lenght
    """
    print('-' * num)

